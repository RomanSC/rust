fn main () {
    // let n be equal to 5
    let n = 5;

    // operators
    println!("Printing operators results.");
    println!("{}", (n));
    println!("{}", (n + 5));
    println!("{}", (n - 5));
    println!("{}", (n * 5));
    println!("{}", (n / 5));
    println!("{}", (n % 5));

    // comparison
    println!("Printing comparisons results.");
    println!("{}", (n == 5));
    println!("{}", (n != 5));
    println!("{}", (n < 5));
    println!("{}", (n > 5));
    println!("{}", (n <= 5));
    println!("{}", (n >= 5));
}

