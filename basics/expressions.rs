fn main () {
    // Let n be equal to 5
    let n = 10;

    // Operators
    println!("Printing operators results.");
    println!("n = {}", (n));
    println!("n + 5 = {}", (n + 5)); // Addition
    println!("n - 5 = {}", (n - 5)); // Subtraction
    println!("n * 5 = {}", (n * 5)); // Multiplication
    println!("n / 5 = {}", (n / 5)); // Division
    println!("n % 5 = {}", (n % 5)); // Floor division, returns remainder

    // Comparison
    println!("Printing comparisons results.");
    println!("n == 5 = {}", (n == 5)); // Equality, returns bool
    println!("n != 5 = {}", (n != 5)); // Not equal returns bool
    println!("n < 5 = {}", (n < 5));   // Less than
    println!("n > 5 = {}", (n > 5));   // Greater than
    println!("n <= 5 = {}", (n <= 5)); // Less than or equal
    println!("n >= 5 = {}", (n >= 5)); // Greater than or equal

}

